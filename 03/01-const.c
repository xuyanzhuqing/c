// 常量

int a = 85;         /* 十进制 */
int b = 0213;       /* 八进制 */
int c = 0x4b;       /* 十六进制 */
int d = 30;         /* 整数 */
unsigned int e = 30u;        /* 无符号整数 */
long f = 30l;        /* 长整数 */
unsigned long g = 30ul;       /* 无符号长整数 */

// 转义字符
char h = '\\';	// \ 字符
char i = '\'';	//' 字符
char j = '\"';	//" 字符
char k = '\?';	//? 字符

// ... and so on

// 字符串常量
char l[] = "hello world";

